_MD_BOLD = re.compile(r"\*\*(.+?)\*\*")
_MD_ITALIC = re.compile(r"\*(.+?)\*")
_MD_CODE = re.compile(r"`([^`]+)`")
_MD_LINK = re.compile(r"\[([^\]]+)\]\(([^)]+)\)")


def _esc(text: str) -> str:
    """HTML-escape text."""
    return html_module.escape(text) if text else ""


def _render_inline(text: str) -> str:
    """Convert inline markdown (bold, italic, code, links) to HTML."""
    if not text:
        return ""
    text = _esc(text)
    # Bold
    text = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", text)
    # Italic
    text = re.sub(r"\*(.+?)\*", r"<em>\1</em>", text)
    # Inline code
    text = re.sub(r"`([^`]+)`", r'<code class="inline-code">\1</code>', text)
    return text


def _render_content_block(content: str, theme: dict) -> str:
    """Render a block of text content into HTML paragraphs, lists, etc."""
    if not content:
        return ""

    html_parts = []
    lines = content.split("\n")
    in_list = False
    list_type = None
    i = 0

    while i < len(lines):
        line = lines[i].strip()

        # Empty line
        if not line:
            if in_list:
                html_parts.append(f"</{list_type}>")
                in_list = False
                list_type = None
            i += 1
            continue

        # Bullet list
        bullet_match = re.match(r"^[-*+]\s+(.+)$", line)
        if bullet_match:
            if not in_list or list_type != "ul":
                if in_list:
                    html_parts.append(f"</{list_type}>")
                html_parts.append("<ul>")
                in_list = True
                list_type = "ul"
            html_parts.append(f"  <li>{_render_inline(bullet_match.group(1))}</li>")
            i += 1
            continue

        # Numbered list
        num_match = re.match(r"^\d+\.\s+(.+)$", line)
        if num_match:
            if not in_list or list_type != "ol":
                if in_list:
                    html_parts.append(f"</{list_type}>")
                html_parts.append("<ol>")
                in_list = True
                list_type = "ol"
            html_parts.append(f"  <li>{_render_inline(num_match.group(1))}</li>")
            i += 1
            continue

        # Blockquote
        if line.startswith(">"):
            if in_list:
                html_parts.append(f"</{list_type}>")
                in_list = False
            quote_text = line.lstrip("> ").strip()
            html_parts.append(f'<blockquote>{_render_inline(quote_text)}</blockquote>')
            i += 1
            continue

        # Horizontal rule
        if re.match(r"^[-*_]{3,}$", line):
            if in_list:
                html_parts.append(f"</{list_type}>")
                in_list = False
            html_parts.append("<hr>")
            i += 1
            continue

        # Regular paragraph
        if in_list:
            html_parts.append(f"</{list_type}>")
            in_list = False
            list_type = None
        html_parts.append(f"<p>{_render_inline(line)}</p>")
        i += 1

    if in_list:
        html_parts.append(f"</{list_type}>")

    return "\n".join(html_parts)


def _render_table(table: dict, theme: dict) -> str:
    """Render a structured table dict into an HTML table."""
    if not table.get("headers"):
        return ""

    html = ['<div class="table-wrapper"><table>']
    html.append("  <thead><tr>")
    for header in table["headers"]:
        html.append(f"    <th>{_esc(header)}</th>")
    html.append("  </tr></thead>")
    html.append("  <tbody>")
    for row_idx, row in enumerate(table.get("rows", [])):
        row_class = "stripe" if row_idx % 2 == 1 else ""
        html.append(f'  <tr class="{row_class}">')
        for cell in row:
            html.append(f"    <td>{_render_inline(cell)}</td>")
        html.append("  </tr>")
    html.append("  </tbody>")
    html.append("</table></div>")
    return "\n".join(html)


def _render_code_block(code: dict, theme: dict) -> str:
    """Render a code block with macOS-style header and language badge."""
    lang = code.get("language", "")
    code_text = _esc(code.get("code", ""))
    lang_badge = f'<span class="code-lang-badge">{_esc(lang)}</span>' if lang else ""
    return f"""<div class="code-block">
  <div class="code-block-header">
    <span class="code-block-dot" style="background:#ff5f57"></span>
    <span class="code-block-dot" style="background:#febc2e"></span>
    <span class="code-block-dot" style="background:#28c840"></span>
    {lang_badge}
  </div>
  <pre><code>{code_text}</code></pre>
</div>"""


