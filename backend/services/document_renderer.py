def render_document_html(
    doc_json: dict,
    theme: str = "professional",
    embed_css: bool = True,
    for_preview: bool = False,
) -> str:
    """
    Convert a structured JSON document into themed HTML.

    Args:
        doc_json: Structured document from document_parser.py
        theme: Theme name ('professional', 'dark', 'minimal')
        embed_css: If True, includes <style> block (for standalone HTML)
        for_preview: If True, wraps in a div instead of full HTML document

    Returns:
        Complete HTML string
    """
    t = THEMES.get(theme, THEMES["professional"])
    title = doc_json.get("title", "Untitled Document")
    generated_at = doc_json.get("generated_at", datetime.utcnow().isoformat())
    sections = doc_json.get("sections", [])
    toc = doc_json.get("table_of_contents", [])
    metadata = doc_json.get("metadata", {})

    # Format date
    try:
        dt = datetime.fromisoformat(generated_at.replace("Z", "+00:00"))
        date_str = dt.strftime("%B %d, %Y at %H:%M UTC")
    except (ValueError, AttributeError):
        date_str = generated_at

    html_parts = []

    # ── Title Page ────────────────────────────────────────────────────────────
    html_parts.append(f"""<div class="doc-title-page">
  <h1>{_esc(title)}</h1>
  <div class="doc-subtitle">{_esc(str(metadata.get('total_sections', 0)))} Sections · {_esc(str(metadata.get('total_pages_estimate', 1)))} Pages Estimated</div>
  <div class="doc-date">Generated on {_esc(date_str)}</div>
  <div class="doc-brand">LACUNEX AI</div>
</div>""")

    # ── Table of Contents ─────────────────────────────────────────────────────
    if toc:
        html_parts.append('<div class="doc-toc">')
        html_parts.append("  <h2>Table of Contents</h2>")
        html_parts.append("  <ul>")
        for idx, entry in enumerate(toc):
            num = idx + 1
            html_parts.append(
                f'    <li><span class="toc-number">{num}.</span>'
                f'<a href="#section-{idx}">{_esc(entry["title"])}</a></li>'
            )
            for child in entry.get("children", []):
                html_parts.append(
                    f'    <li class="toc-sub"><span class="toc-number"></span>'
                    f'<a href="#section-{idx}-{child["index"]}">{_esc(child["title"])}</a></li>'
                )
        html_parts.append("  </ul>")
        html_parts.append("</div>")

    # ── Sections ──────────────────────────────────────────────────────────────
    for idx, section in enumerate(sections):
        html_parts.append(f'<div id="section-{idx}">')
        html_parts.append(_render_section(section, t, is_first=(idx == 0)))
        html_parts.append("</div>")

    # ── Footer ────────────────────────────────────────────────────────────────
    html_parts.append(f'<div class="doc-footer">{_esc(BRAND_FOOTER)}</div>')

    body_html = "\n".join(html_parts)

    if for_preview:
        # Return div-wrapped HTML for embedding in frontend preview
        css = get_theme_css(theme) if embed_css else ""
        return f"""<div class="document-root" data-theme="{_esc(theme)}">
<style>{css}</style>
{body_html}
</div>"""

    # Full standalone HTML document
    css = get_theme_css(theme)
    return f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>{_esc(title)} — LACUNEX AI</title>
  <style>{css}</style>
</head>
<body>
  <div class="document-root" style="max-width: 900px; margin: 0 auto; padding: 40px 32px;">
    {body_html}
  </div>
</body>
</html>"""


def get_available_themes() -> list[dict]:
    """Return list of available theme descriptors for the frontend."""
    return [
        {"id": key, "name": val["name"]}
        for key, val in THEMES.items()
    ]
